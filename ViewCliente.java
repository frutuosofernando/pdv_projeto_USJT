    private void salvarCliente(){
        modelCliente.setCliNome(this.jtfNome.getText().toUpperCase());
        modelCliente.setCliEndereco(this.jtfEndereco.getText());
        modelCliente.setCliBairro(this.jtfBairro.getText());
        modelCliente.setCliCidade(this.jtfCidade.getText());
        modelCliente.setCliUf(this.jcbUF.getSelectedItem().toString());
        modelCliente.setCliCep(this.jtfCEP.getText());
        modelCliente.setCliTelefone(this.jtfTelefone.getText());
        
        if(controllerCliente.salvarClientesController(modelCliente)>0){
            JOptionPane.showMessageDialog(this,"Cliente cadastrado com sucesso"); 
            carregarCliente();
            limparCampos();
            habilitarDesabilitarCampos(false);
        } else{
            JOptionPane.showMessageDialog(this, "Erro no cadastro", "ERRO", JOptionPane.ERROR_MESSAGE);       
        }
    } 
    
    private void alterarCliente(){
        modelCliente.setCliNome(this.jtfNome.getText().toUpperCase());
        modelCliente.setCliEndereco(this.jtfEndereco.getText());
        modelCliente.setCliBairro(this.jtfBairro.getText());
        modelCliente.setCliCidade(this.jtfCidade.getText());
        modelCliente.setCliUf(this.jcbUF.getSelectedItem().toString());
        modelCliente.setCliCep(this.jtfCEP.getText());
        modelCliente.setCliTelefone(this.jtfTelefone.getText());
        if(controllerCliente.alterarClienteController(modelCliente)){
            JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso!");
            carregarCliente();
            limparCampos();
            habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao alterar o registro do cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }  
    }
    
    private void habilitarDesabilitarCampos(boolean condicao){
        jtfCodigo.setEnabled(false);
        jtfNome.setEnabled(condicao);
        jtfEndereco.setEnabled(condicao);
        jtfBairro.setEnabled(condicao);
        jtfCidade.setEnabled(condicao);
        jtfCEP.setEnabled(condicao);
        jtfTelefone.setEnabled(condicao);
        jcbUF.setEnabled(condicao);
        
    }
    
    private void limparCampos(){
        jtfCodigo.setText("");
        jtfNome.setText("");
        jtfEndereco.setText("");
        jtfBairro.setText("");       
        jtfCidade.setText("");  
        jtfCEP.setText("");  
        jtfTelefone.setText("");
        jcbUF.setSelectedItem(null);
    }
    
    
    private void carregarCliente(){
        listaModelCliente = controllerCliente.retornarListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) jtCliente.getModel();
        modelo.setNumRows(0);
        int cont = listaModelCliente.size();
        for (int i = 0; i<cont; i++){
            modelo.addRow(new Object[]{
                listaModelCliente.get(i).getIdCliente(),
                listaModelCliente.get(i).getCliNome(),
                listaModelCliente.get(i).getCliCidade(),
                listaModelCliente.get(i).getCliTelefone()
            });
        }    
        
    }

