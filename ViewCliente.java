    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // Alterar registro de um cliente:
        salvarAlterar = "alterar";
        int linha = this.jtCliente.getSelectedRow();
        int codigoCliente = (int) this.jtCliente.getValueAt(linha,0);
        try{
            // Recuperar dados do banco
            modelCliente = controllerCliente.retornarClienteController(codigoCliente);
            // Coloca na interface
            this.jtfCodigo.setText(String.valueOf(modelCliente.getIdCliente()));
            this.jtfNome.setText(modelCliente.getCliNome());
            this.jtfEndereco.setText(modelCliente.getCliEndereco());
            this.jtfBairro.setText(modelCliente.getCliBairro());
            this.jtfCidade.setText(modelCliente.getCliCidade());
            this.jcbUF.setSelectedItem(String.valueOf(modelCliente.getCliUf()));
            this.jtfCEP.setText(modelCliente.getCliCep());
            this.jtfTelefone.setText(modelCliente.getCliTelefone());
        } catch(Exception e){
            JOptionPane.showMessageDialog(this,"Código inválido ou nenhum registro selecionado", "AVISO", JOptionPane.ERROR_MESSAGE);
        }
        habilitarDesabilitarCampos(true);
        
    }//GEN-LAST:event_jbAlterarActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // Salvar cliente
        if(salvarAlterar.equals("salvar")){
            this.salvarCliente();
        } else if(salvarAlterar.equals("alterar")){
            this.alterarCliente();
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jbCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCancelarActionPerformed
        // Cancela a inserção ou alteração de um registro:
        this.habilitarDesabilitarCampos(false);
        this.limparCampos(); 
    }//GEN-LAST:event_jbCancelarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // Exclui cliente:
        int linha = jtCliente.getSelectedRow();
        int codigoCliente = (int) jtCliente.getValueAt(linha, 0);
        if (controllerCliente.excluiClientesController(codigoCliente)){
            JOptionPane.showMessageDialog(this, "Cliente excluido com sucesso!");
            this.carregarCliente();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao excluir o cliente!","ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        //Procura o cliente com o nome correspondente
        DefaultTableModel modelo = (DefaultTableModel) this.jtCliente.getModel();
        final TableRowSorter<TableModel> classificator = new TableRowSorter<>(modelo);
        this.jtCliente.setRowSorter(classificator);
        String texto = jtfPesquisar.getText().toUpperCase();
        classificator.setRowFilter(RowFilter.regexFilter(texto, 1));
    }//GEN-LAST:event_jbPesquisarActionPerformed

